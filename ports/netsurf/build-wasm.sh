#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
PORT_DIR="$ROOT_DIR/ports/netsurf"
VENDOR_DIR="$PORT_DIR/vendor"
PREFIX="$PORT_DIR/inst"
LOG_DIR="$PORT_DIR/build-logs"
PUBLIC_DIR="$ROOT_DIR/public/browsers/netsurf"

require_tool() {
  if ! command -v "$1" >/dev/null 2>&1; then
    echo "Missing required tool: $1" >&2
    echo "Run ports/netsurf/install-toolchain-apt.sh on Ubuntu, or install Emscripten manually." >&2
    exit 1
  fi
}

require_tool git
require_tool make
require_tool emcc
require_tool emar
require_tool emranlib
require_tool emconfigure
require_tool emmake
require_tool gperf

mkdir -p "$VENDOR_DIR" "$PREFIX" "$LOG_DIR" "$PUBLIC_DIR" "$PORT_DIR/shims/curl"

clone_at() {
  local name="$1" url="$2" rev="$3"
  local dest="$VENDOR_DIR/$name"
  if [ ! -d "$dest/.git" ]; then
    git init "$dest" >/dev/null
    git -C "$dest" remote add origin "$url"
  fi
  if [ "$(git -C "$dest" rev-parse HEAD 2>/dev/null || true)" != "$rev" ]; then
    git -C "$dest" fetch --depth 1 origin "$rev"
    git -C "$dest" checkout --detach FETCH_HEAD
  fi
}

clone_at buildsystem https://github.com/netsurf-browser/buildsystem.git 0005ae300283ff01c2e2b05e7376b3e55dea21f7
clone_at netsurf https://github.com/netsurf-browser/netsurf.git 39da3c3a40af4566d86500ff3052dfdc7f9a0378
clone_at libnsutils https://github.com/netsurf-browser/libnsutils.git 0bd39060740b6163bd50875326654a722df97eb2
clone_at libnslog https://github.com/netsurf-browser/libnslog.git bedff2146270a8a73cc265bab46ec39f9c170d07
clone_at libnspsl https://github.com/netsurf-browser/libnspsl.git 82815c2bc7fd70d1b6afccfa89a9a0f3fa73db8a
clone_at libnsbmp https://github.com/netsurf-browser/libnsbmp.git ea063c9f46acb43e90208da14073332b505ef7e7
clone_at libnsgif https://github.com/netsurf-browser/libnsgif.git 5d5d750f32755d415fd232c607e6ef64dcc5aa8a
clone_at libparserutils https://github.com/netsurf-browser/libparserutils.git 6b0cbf086ca8eb8fe74b69f0c9ecf274eb2397ca
clone_at libwapcaplet https://github.com/netsurf-browser/libwapcaplet.git c7c128d3eb3223b216c974471f82e9337fbcf4ba
clone_at libhubbub https://github.com/netsurf-browser/libhubbub.git 6651b8cf87a4aa87bcdb2ff024a02659cd3f9402
clone_at libcss https://github.com/netsurf-browser/libcss.git 104d87fde48b9e022cd3cdad28aeb4d8cc0a0c5a
clone_at libdom https://github.com/netsurf-browser/libdom.git f69781e1f062444b5af3f62d431d7d94018da53b
clone_at libnsfb https://github.com/netsurf-browser/libnsfb.git b701cdce7241c3747ccd78658a365db0983ebe24
clone_at zlib https://github.com/madler/zlib.git e3dc0a85b7032e98380dec011bc8f2c2ee0d8fca

cat > "$PORT_DIR/shims/curl/curl.h" <<'SHIM'
#ifndef BPE_NETSURF_WASM_CURL_SHIM_H
#define BPE_NETSURF_WASM_CURL_SHIM_H
typedef struct CURLM CURLM;
#endif
SHIM

rm -rf "$PREFIX"
mkdir -p "$PREFIX" "$LOG_DIR"

make -C "$VENDOR_DIR/buildsystem" install PREFIX="$PREFIX" >"$LOG_DIR/00-buildsystem.log" 2>&1

export PREFIX
export NSSHARED="$PREFIX/share/netsurf-buildsystem"
export PKG_CONFIG_PATH="$PREFIX/lib/pkgconfig:${PKG_CONFIG_PATH:-}"
export CC=emcc
export CXX=em++
export AR=emar
export LD=emcc
export RANLIB=emranlib

# Debian's emscripten package has a frozen global cache, so build zlib from source
# instead of relying on `-sUSE_ZLIB` to fetch an Emscripten port.
(
  cd "$VENDOR_DIR/zlib"
  emconfigure ./configure --static --prefix="$PREFIX" >"$LOG_DIR/zlib-configure.log" 2>&1
  emmake make -j"$(nproc)" >"$LOG_DIR/zlib-build.log" 2>&1
  emmake make install >"$LOG_DIR/zlib-install.log" 2>&1
)

for lib in \
  libnsutils \
  libnslog \
  libnspsl \
  libnsbmp \
  libnsgif \
  libparserutils \
  libwapcaplet \
  libhubbub \
  libcss \
  libdom \
  libnsfb; do
  echo "==> Building $lib"
  make -C "$VENDOR_DIR/$lib" clean PREFIX="$PREFIX" NSSHARED="$NSSHARED" toolchain=clang >/dev/null 2>&1 || true
  extra=()
  if [ "$lib" = libdom ]; then
    # Avoid an additional expat/libxml cross build for the first HTML-only probe.
    extra+=(WITH_EXPAT_BINDING=no)
  fi
  make -C "$VENDOR_DIR/$lib" install \
    PREFIX="$PREFIX" \
    NSSHARED="$NSSHARED" \
    COMPONENT_TYPE=lib-static \
    VARIANT=release \
    toolchain=clang \
    "${extra[@]}" \
    >"$LOG_DIR/${lib}.log" 2>&1
done

cat > "$VENDOR_DIR/netsurf/Makefile.config" <<EOF_CONFIG
# Generated by ports/netsurf/build-wasm.sh.
override NETSURF_USE_CURL := NO
override NETSURF_USE_OPENSSL := NO
override NETSURF_USE_JPEG := NO
override NETSURF_USE_PNG := NO
override NETSURF_USE_BMP := YES
override NETSURF_USE_GIF := YES
override NETSURF_USE_NSSVG := NO
override NETSURF_USE_ROSPRITE := NO
override NETSURF_USE_WEBP := NO
override NETSURF_USE_JPEGXL := NO
override NETSURF_USE_DUKTAPE := NO
override NETSURF_USE_NSPSL := YES
override NETSURF_USE_NSLOG := YES
override NETSURF_USE_UTF8PROC := NO
override NETSURF_USE_LIBICONV_PLUG := YES
override NETSURF_FB_FONTLIB := internal
override NETSURF_FB_FRONTEND := ram
override NETSURF_HOMEPAGE := "about:blank"
CFLAGS += -O2 -I../../shims
LDFLAGS += -sALLOW_MEMORY_GROWTH=1 -sEXIT_RUNTIME=1
EOF_CONFIG

(
  cd "$VENDOR_DIR/netsurf"
  make clean TARGET=framebuffer TOOLCHAIN=clang >/dev/null 2>&1 || true
  make TARGET=framebuffer PREFIX="$PREFIX" NSSHARED="$NSSHARED" TOOLCHAIN=clang V=1 \
    >"$LOG_DIR/netsurf-framebuffer.log" 2>&1
)

cp "$VENDOR_DIR/netsurf/nsfb" "$PUBLIC_DIR/nsfb.js"
cp "$VENDOR_DIR/netsurf/nsfb.wasm" "$PUBLIC_DIR/nsfb.wasm"
cat > "$PUBLIC_DIR/build-manifest.txt" <<EOF_MANIFEST
Built by ports/netsurf/build-wasm.sh
NetSurf commit: 39da3c3a40af4566d86500ff3052dfdc7f9a0378
Frontend: framebuffer with libnsfb RAM surface
Networking: CURL disabled; offline about:, data:, file/resource fetchers only
Generated: $(date -u +%Y-%m-%dT%H:%M:%SZ)
EOF_MANIFEST

ls -lh "$PUBLIC_DIR/nsfb.js" "$PUBLIC_DIR/nsfb.wasm"
